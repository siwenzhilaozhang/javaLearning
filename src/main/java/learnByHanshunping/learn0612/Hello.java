package learnByHanshunping.learn0612;
//public：表示这个类是公共的，一个java文件中只能有一个公共类
//class：表示这是一个类
//Hello：类名（公共类的类名必须和文件名一致）

public class Hello {
    //一个主函数，相当于是程序的入口
    public static void main(String args[]){
        //执行语句
       // System.out.println("hello");
        int a=10; //定义一个变量，变量名为a，值为10
        int b=20;
        int result = a+b;
        System.out.println("结果是"+result);
    }
}
